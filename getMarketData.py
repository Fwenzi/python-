class MarketData(object):
	def __init__(self, whichPage):
		super(MarketData, self).__init__()
		self.whichPage = whichPage

	# 读入数据库
	def createTable(self):
		conn = sqlite3.connect("LTS.db")
		cur = conn.cursor()

		try:
			cur.execute('''CREATE TABLE stockList
      (id text primary key,
       name text,
       newPrice text,
       priceLimit text,
       priceChange text,
       fiveChange text)''')
		except:
			print('table is exist')
		finally:
			pass

		p = []
		last_stock = self.getData()
		for i in range(0,len(last_stock),13):
			l = (last_stock[i],last_stock[i+1],last_stock[i+2],last_stock[i+3],last_stock[i+4],last_stock[i+5])
			print(l)
			p.append(l)
		cur.executemany("INSERT INTO stockList VALUES (?,?,?,?,?,?)",p)
		cur.close()
		conn.commit()
		conn.close()
		print('success')

# 获取爬虫数据
	def getData(self):
		url='http://quote.stockstar.com/stock/ranklist_a_3_1_'+str(self.whichPage)+'.html'
		#随机从user_agent列表中抽取一个元素
		request=urllib.request.Request(url=url,headers={"User-Agent":random.choice(user_agent)})
		try:
			response=urllib.request.urlopen(request)
		#异常检测
		except urllib.error.HTTPError as e:
			print(e.code)
		except urllib.error.URLError as e:
			print(e.reason)
		#读取网页内容
		content=response.read().decode('gbk')
		print('get page')
		pattern=re.compile('<tbody[\s\S]*</tbody>')
		body=re.findall(pattern,str(content))
		pattern=re.compile('>(.*?)<')
		#正则匹配
		stock_page=re.findall(pattern,body[0])
		total_stock_market.extend(stock_page)
		time.sleep(random.randrange(1,4))  
		#删除空白字符
		last_stock_market=total_stock_market[:]  
		for data in total_stock_market:
			if data=='':
				last_stock_market.remove('')
		#返回数据
		return last_stock_market


